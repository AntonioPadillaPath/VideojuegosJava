    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        // TODO add your handling code here:
        
        float peso, estatura, imc;
        String estado;
        String mensaje1;
        String nombre, apellido, edad;
        
        peso = new Float(Peso.getText());
        estatura = new Float(Estatura.getText());
        nombre = new String(Nombre.getText());
        apellido = new String(Apellido.getText());
        edad = new String(Edad.getText());
        
        imc = peso/(estatura * estatura);
        
        if (imc < 18.5)
        {
            estado = "Delgadez";
        }
        else if (imc >= 18.5 && imc <= 24.99)
        {
            estado = "Normal";
        }
        else if (imc >= 25 && imc <= 30)
        {
            estado = "Soprepeso";
        }
        else
        {
            estado = "Obesidad";
        }
        
        
        mensaje1 = String.format("%2.2f", imc);
        IMC.setText(mensaje1);
        Estado.setText(estado);
        
        JOptionPane.showMessageDialog(null, "Nombre: " +nombre+ "\n"
                + "Apellido: " +apellido+ "\n"
                        + "Edad: " +edad+ "\n"
                                + "Peso: " +peso+ "\n"
                                        + "Estatura: " +estatura+ "\n"
                                                + "IMC: " +imc+ ", " +estado, "Ficha", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_CalcularActionPerformed

